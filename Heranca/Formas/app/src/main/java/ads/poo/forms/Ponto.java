package ads.poo.forms;

public class Ponto {

    private double x;
    private double y;

    public Ponto() {
        this.x = x;
        this.y = y;
    }

    public Ponto(double x, double y) {
        this();
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
